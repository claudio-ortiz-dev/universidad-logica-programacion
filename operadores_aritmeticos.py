# Operadores Aritmeticos

# Suma +
a = 3 + 4
print("Suma:", a)

# Resta -
b = 6 - 2
print("Resta:", b)

# Multiplicacion *
c = a * 2
print("Multiplicacion:", c)

# Division /
d = b / c
print("Division:", d)

# Potencia **  (NO es ^ en Python)
e = a ** 2
print("Potencia:", e)

# Modulo %
f = 9 % 3
print("Modulo:", f)
